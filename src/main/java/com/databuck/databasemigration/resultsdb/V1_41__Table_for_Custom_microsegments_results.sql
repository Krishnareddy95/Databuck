    Id bigint(20) NOT NULL AUTO_INCREMENT,
    idApp int(11) DEFAULT NULL,
    Date date DEFAULT NULL,
    Run bigint(20) DEFAULT NULL,
    dayOfYear int(100) DEFAULT NULL,
    month varchar(50) DEFAULT NULL,
    dayOfMonth int(50) DEFAULT NULL,
    dayOfWeek varchar(50) DEFAULT NULL,
    hourOfDay int(50) DEFAULT NULL,
    ColName varchar(255) DEFAULT NULL,
    Count bigint(20) DEFAULT NULL,
    Min double DEFAULT NULL,
    Max double DEFAULT NULL,
    Cardinality int(50) DEFAULT NULL,
    Std_Dev double DEFAULT NULL,
    Mean double DEFAULT NULL,
    Null_Value bigint(20) DEFAULT NULL,
    Status varchar(20) DEFAULT NULL,
    StringCardinalityAvg double DEFAULT NULL,
    StringCardinalityStdDev double DEFAULT NULL,
    StrCardinalityDeviation double DEFAULT NULL,
    String_Threshold double DEFAULT NULL,
    String_Status varchar(50) DEFAULT NULL,
    NumMeanAvg double DEFAULT NULL,
    NumMeanStdDev double DEFAULT NULL,
    NumMeanDeviation double DEFAULT NULL,
    NumMeanThreshold double DEFAULT NULL,
    NumMeanStatus varchar(50) DEFAULT NULL,
    NumSDAvg double DEFAULT NULL,
    NumSDStdDev double DEFAULT NULL,
    NumSDDeviation double DEFAULT NULL,
    NumSDThreshold double DEFAULT NULL,
    NumSDStatus varchar(50) DEFAULT NULL,
    outOfNormStatStatus varchar(100) DEFAULT NULL,
    sumOfNumStat double DEFAULT NULL,
    NumSumAvg double DEFAULT NULL,
    NumSumStdDev double DEFAULT NULL,
    NumSumThreshold double DEFAULT NULL,
    dGroupVal varchar(255) DEFAULT NULL,
    dGroupCol varchar(255) DEFAULT NULL,
    dataDriftCount bigint(200) DEFAULT NULL,
    dataDriftStatus varchar(50) DEFAULT NULL,
    Default_Value bigint(20) DEFAULT NULL,
    Default_Count bigint(20) DEFAULT NULL,
    Record_Count bigint(20) DEFAULT NULL,
    Null_Percentage double DEFAULT NULL,
    Default_Percentage double DEFAULT NULL,
    Null_Threshold double DEFAULT NULL,
    Default_Threshold double DEFAULT NULL,
    forgot_run_enabled varchar(10) DEFAULT 'N',
    PRIMARY KEY (Id),
    KEY idApp_index (idApp),
    KEY Date_index (Date),
    KEY dayOfWeek_index (dayOfWeek),
    KEY ColName_index (ColName),
    KEY dGroupVal_index (dGroupVal)
  ) ENGINE=InnoDB AUTO_INCREMENT=256 DEFAULT CHARSET=utf8;


