package com.databuck.dto;

public class AverageDQI {

	Long avgDQI;
	String date;
	Integer run;
	
	public Long getAvgDQI() {
		return avgDQI;
	}
	public void setAvgDQI(Long avgDQI) {
		this.avgDQI = avgDQI;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public Integer getRun() {
		return run;
	}
	public void setRun(Integer run) {
		this.run = run;
	}
	
	
}
