package com.databuck.csvmodel;

public class SequenceCheck {

	private String idApp;
	private String date;
	private String run;
	private String sDate;
	private String eDate;
	private String timelinessKey;
	private String noOfDays;
	private String status;
	private String totalCount;
	private String totalFailedCount;
	private String forgotRunEnabled;

	public String getIdApp() {
		return idApp;
	}

	public void setIdApp(String idApp) {
		this.idApp = idApp;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getRun() {
		return run;
	}

	public void setRun(String run) {
		this.run = run;
	}

	public String getsDate() {
		return sDate;
	}

	public void setsDate(String sDate) {
		this.sDate = sDate;
	}

	public String geteDate() {
		return eDate;
	}

	public void seteDate(String eDate) {
		this.eDate = eDate;
	}

	public String getTimelinessKey() {
		return timelinessKey;
	}

	public void setTimelinessKey(String timelinessKey) {
		this.timelinessKey = timelinessKey;
	}

	public String getNoOfDays() {
		return noOfDays;
	}

	public void setNoOfDays(String noOfDays) {
		this.noOfDays = noOfDays;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getTotalCount() {
		return totalCount;
	}

	public void setTotalCount(String totalCount) {
		this.totalCount = totalCount;
	}

	public String getTotalFailedCount() {
		return totalFailedCount;
	}

	public void setTotalFailedCount(String totalFailedCount) {
		this.totalFailedCount = totalFailedCount;
	}

	public String getForgotRunEnabled() {
		return forgotRunEnabled;
	}

	public void setForgotRunEnabled(String forgotRunEnabled) {
		this.forgotRunEnabled = forgotRunEnabled;
	}

}
