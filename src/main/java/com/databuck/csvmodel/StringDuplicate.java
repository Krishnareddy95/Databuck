package com.databuck.csvmodel;

public class StringDuplicate {
	private String idApp;
	private String date;
	private String uniqueValues;
	private String colName;
	private String dGroupVal;
	private String forgotRunEnabled;
	private String dGroupCol;
	private String run;
	private String id;
	private String potentialDuplicates;

	public String getIdApp() {
		return idApp;
	}

	public void setIdApp(String idApp) {
		this.idApp = idApp;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getUniqueValues() {
		return uniqueValues;
	}

	public void setUniqueValues(String uniqueValues) {
		this.uniqueValues = uniqueValues;
	}

	public String getColName() {
		return colName;
	}

	public void setColName(String colName) {
		this.colName = colName;
	}

	public String getMicrosegmentValue() {
		return dGroupVal;
	}

	public void setDGroupVal(String dGroupVal) {
		this.dGroupVal = dGroupVal;
	}

	public String getForgotRunEnabled() {
		return forgotRunEnabled;
	}

	public void setForgotRunEnabled(String forgotRunEnabled) {
		this.forgotRunEnabled = forgotRunEnabled;
	}

	public String getMicrosegment() {
		return dGroupCol;
	}

	public void setDGroupCol(String dGroupCol) {
		this.dGroupCol = dGroupCol;
	}

	public String getRun() {
		return run;
	}

	public void setRun(String run) {
		this.run = run;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPotentialDuplicates() {
		return potentialDuplicates;
	}

	public void setPotentialDuplicates(String potentialDuplicates) {
		this.potentialDuplicates = potentialDuplicates;
	}

}
