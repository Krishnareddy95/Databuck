package com.databuck.csvmodel;

public class AdvDataDriftCountSummary {
	private String idApp;
	private String date;
	private String colName;
	private String forgotRunEnabled;
	private String missingValueCount;
	private String run;
	private String id;
	private String newValueCount;
	private String uniqueValuesCount;

	public String getIdApp() {
		return idApp;
	}

	public void setIdApp(String idApp) {
		this.idApp = idApp;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getColName() {
		return colName;
	}

	public void setColName(String colName) {
		this.colName = colName;
	}

	public String getForgotRunEnabled() {
		return forgotRunEnabled;
	}

	public void setForgotRunEnabled(String forgotRunEnabled) {
		this.forgotRunEnabled = forgotRunEnabled;
	}

	public String getMissingValueCount() {
		return missingValueCount;
	}

	public void setMissingValueCount(String missingValueCount) {
		this.missingValueCount = missingValueCount;
	}

	public String getRun() {
		return run;
	}

	public void setRun(String run) {
		this.run = run;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNewValueCount() {
		return newValueCount;
	}

	public void setNewValueCount(String newValueCount) {
		this.newValueCount = newValueCount;
	}

	public String getUniqueValuesCount() {
		return uniqueValuesCount;
	}

	public void setUniqueValuesCount(String uniqueValuesCount) {
		this.uniqueValuesCount = uniqueValuesCount;
	}

}
